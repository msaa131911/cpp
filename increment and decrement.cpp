#include <iostream>
using namespace std;

int main() 
{
   int x,y;
   x=3;
   //y= x++;আগে ব্যবহার করে, পরে বাড়ায়
   
   y=++x;//আগে বাড়ায়, পরে ব্যবহার করে
   
   cout<<y <<endl;
   
   //y= x--;আগে ব্যবহার করে, পরে কমায়
   
   y= --x;//আগে কমায়, পরে ব্যবহার করে
   
   cout<<y <<endl;
   
    
   
   
  
   
}